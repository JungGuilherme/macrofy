-- ============================================================
-- BLOCO 1: Cache de cotações (sparklines dos cards da home)
-- ============================================================
-- Cache de cotações (Yahoo Finance via GitHub Actions, a cada 15 min)
-- ⚠️ Banco Lovable Cloud: cole no chat do Lovable pedindo "run this SQL".

CREATE TABLE IF NOT EXISTS public.market_quotes (
  symbol text PRIMARY KEY,
  name text NOT NULL,
  price numeric NOT NULL,
  change numeric NOT NULL DEFAULT 0,
  change_percent numeric NOT NULL DEFAULT 0,
  currency text DEFAULT 'USD',
  updated_at timestamptz DEFAULT now()
);

-- Intraday closes for the card sparkline (added 2026-07). Safe to re-run.
ALTER TABLE public.market_quotes ADD COLUMN IF NOT EXISTS sparkline numeric[] DEFAULT '{}';

ALTER TABLE public.market_quotes ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Authenticated can read market quotes" ON public.market_quotes;
DROP POLICY IF EXISTS "Admins manage market quotes" ON public.market_quotes;

CREATE POLICY "Authenticated can read market quotes"
  ON public.market_quotes FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins manage market quotes"
  ON public.market_quotes FOR ALL TO authenticated
  USING (public.has_role(auth.uid(), 'admin'))
  WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- ============================================================
-- BLOCO 2: Ranking de Ativos (tabela periódica)
-- ============================================================
-- Ranking de Ativos ("tabela periódica" de retornos) — dados reais da carta
-- ⚠️ O banco é Lovable Cloud: cole este arquivo inteiro no chat do Lovable
-- pedindo "run this SQL in my database".
-- Períodos: anos 2016–2025, meses jan/24–mai/26 (sem nov/25 e jun/26 — ausentes
-- na fonte), 'Acum. 2026' (ano corrente) e 'Acumulado' (desde 2016).

CREATE TABLE IF NOT EXISTS public.ranking_assets (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  short_name text NOT NULL,
  color text NOT NULL DEFAULT '#2563eb',
  sort_order integer DEFAULT 0,
  enabled boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  UNIQUE(name)
);

CREATE TABLE IF NOT EXISTS public.ranking_returns (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  asset_id uuid NOT NULL REFERENCES public.ranking_assets(id) ON DELETE CASCADE,
  period text NOT NULL,
  return_pct numeric NOT NULL,
  created_at timestamptz DEFAULT now(),
  UNIQUE(asset_id, period)
);

ALTER TABLE public.ranking_assets ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.ranking_returns ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Authenticated can read ranking assets" ON public.ranking_assets;
DROP POLICY IF EXISTS "Admins manage ranking assets" ON public.ranking_assets;
DROP POLICY IF EXISTS "Authenticated can read ranking returns" ON public.ranking_returns;
DROP POLICY IF EXISTS "Admins manage ranking returns" ON public.ranking_returns;

CREATE POLICY "Authenticated can read ranking assets"
  ON public.ranking_assets FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins manage ranking assets"
  ON public.ranking_assets FOR ALL TO authenticated
  USING (public.has_role(auth.uid(), 'admin'))
  WITH CHECK (public.has_role(auth.uid(), 'admin'));
CREATE POLICY "Authenticated can read ranking returns"
  ON public.ranking_returns FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins manage ranking returns"
  ON public.ranking_returns FOR ALL TO authenticated
  USING (public.has_role(auth.uid(), 'admin'))
  WITH CHECK (public.has_role(auth.uid(), 'admin'));

